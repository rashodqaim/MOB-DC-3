import UIKit

extension UIColor {
    class func vineyardBlue() -> UIColor {
        return UIColor(red:0.0157, green:0.161, blue:0.361, alpha:1)
    }
    
    class func vineyardPink() -> UIColor {
        return UIColor(red:0.945, green:0.635, blue:0.71, alpha:1)
    }
}